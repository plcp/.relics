uint8_t mix[] =
    {0x0, 0x00, 0x01, 0x03, 0x06, 0x0a,
    0x0f, 0x15, 0x1c, 0x24, 0x2d, 0x37,
    0x42, 0x4e, 0x5b, 0x69, 0x78, 0x88};

void rotate(uint8_t key, uint8_t* plain, uint8_t* cipher, size_t size)
{
    short upper, lower;
    uint8_t state = mix[key];
    for(size_t j = 0; j++ < size;)
    {
        lower = (plain[size - j] & 0x0f) * 0x78 + state;
        state = (state + hay[lower]) % 0xff;

        upper = (plain[size - j] >> 0x4) * 0x78 + state;
        state = (state + hay[upper]) % 0xff;

        *((uint64_t*) (cipher + j - 1)) += (field[upper] << 4) + field[lower];
    }
    *((uint64_t*) (cipher + size)) += field[state];
}

size_t encrypt(
    uint8_t* key, size_t ksize, uint8_t* plain, size_t psize, uint8_t** cipher)
{
    (*cipher) = calloc(ksize + psize + 9, sizeof(uint8_t));
    for(size_t j = 0; j++ < ksize;)
    {
        if(!key[ksize - j])
            continue;
        rotate(key[ksize - j], plain, &(*cipher)[j - 1], psize);
    }

    uint8_t last = ksize + psize + 8;
    while(!(*cipher)[last])
        last -= 1;

    cipher = realloc(*cipher, last + 1);
    return last + 1;
}
